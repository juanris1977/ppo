package principal;

public class Punto {
	private int x;
	private int y;
	//inicializa atributos
	public Punto(int x, int y ) {
		this.x=x;
		this.y=y;
	}
	public Punto() {
		x= 10;
		y= 5;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	
	//settet and getter
	

}
