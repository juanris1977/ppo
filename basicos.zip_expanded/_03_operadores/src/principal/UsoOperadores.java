package principal;

public class UsoOperadores {

	public static void main(String[] args) {
		int a=10, b=7, c= 4;
		System.out.println(a/b); //1
		System.out.println(a==b); // false
		System.out.println(a%b); // 3
		System.out.println(a+b*c); //38
		System.out.println(a++); // 10   lo añade a posteriori
		System.out.println(++a); //11	lo añade a priori
	}

}
